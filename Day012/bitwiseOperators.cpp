// Program that demonstrate bitwise operators

#include<iostream>
using namespace std;

int main() {
    cout << "Bitwise AND(&): " << (4 & 8) << endl;
    cout << "Bitwise OR(|): " << (4 | 8) << endl;
    cout << "Bitwise XOR(^): " << (4 ^ 8) << endl;
    cout << "Bitwise Left Shift Operator(<<): " << (8 << 1) << endl;
    cout << "Bitwise Right Shift Operator(>>): " << (10 >> 2) << endl;

    return 0;
}